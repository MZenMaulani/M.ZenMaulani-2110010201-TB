    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        //Mengecek pemilihan data
        if (tfId.getText().equals("") || tfNama.getText().equals("") ||
                tfNoTelpon.getText().equals("") || taAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih Data Terlebih Dahulu..!",
                "Error", JOptionPane.WARNING_MESSAGE);
        }else{

            //Konfirmasi ubah data
            int hapus = JOptionPane.showConfirmDialog(null, "Anda yakin mengubah data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (hapus == JOptionPane.YES_OPTION) {

                try {
                    // Mengubah data dalam database
                    pst = con.prepareStatement("UPDATE pemasok SET id=?, nama=?, no_telepon=?, alamat=? WHERE id=?");
                    int id   = Integer.parseInt(tfId.getText());
                    pst.setString(1, tfId.getText());
                    pst.setString(2, tfNama.getText());
                    pst.setString(3, tfNoTelpon.getText());
                    pst.setString(4, taAlamat.getText());
                    pst.setString(5, tfId.getText());

                    pst.executeUpdate();
                    pst.close();

                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");


            // Mengubah data dalam row di tabel
            int selectedRow = tblPemasok.getSelectedRow();
            if (selectedRow >= 0) {
                DefaultTableModel model = (DefaultTableModel) tblPemasok.getModel();
                model.setValueAt(tfId.getText(), selectedRow, 0);
                model.setValueAt(tfNama.getText(), selectedRow, 1);
                model.setValueAt(tfNoTelpon.getText(), selectedRow, 2);
                model.setValueAt(taAlamat.getText(), selectedRow, 3); 
                    }
                    //Membersihkan field setelah data berhasil diubah
                    tfId.setText("");
                    tfNama.setText("");
                    tfNoTelpon.setText("");
                    taAlamat.setText("");
                    tfId.requestFocus();

                } catch (SQLException ex) {
                    Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        //Mengecek pemilihan data
        if (tfId.getText().equals("") || tfNama.getText().equals("") ||
                tfNoTelpon.getText().equals("") || taAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Pilih Data Terlebih Dahulu..!",
                "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            //Konfirmasi hapus data
            int ubah = JOptionPane.showConfirmDialog(null, "Anda yakin menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (ubah == JOptionPane.YES_OPTION) {

                // Menghapus data dalam database
                try {
                    pst = con.prepareStatement("DELETE FROM pemasok WHERE id=?");
                    pst.setString(1, tfId.getText());
                    pst.executeUpdate();
                    pst.close(); // Menutup PreparedStatement setelah digunakan
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");

                } catch (SQLException ex) {
                    Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
                }

                           // Menghitung jumlah pemasok
            try{
                pst = con.prepareStatement("SELECT COUNT(*) AS jumlahPemasok FROM pemasok");
                ResultSet rs = pst.executeQuery();
                while(rs.next())
                {
                    int count = rs.getInt("jumlahPemasok");

                    lblJumlah.setText(String.valueOf(count));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Buku.class.getName()).log(Level.SEVERE, null, ex);
            }
            
                //Membersihkan inputan
                tfId.setText("");
                tfNama.setText("");
                tfNoTelpon.setText("");
                taAlamat.setText("");
                tfId.requestFocus();
                //Menghapus data dalam row
                int getSelectedRowForDeletion = tblPemasok.getSelectedRow();
                if (getSelectedRowForDeletion >= 0) {
                    DefaultTableModel model = (DefaultTableModel) tblPemasok.getModel();
                    model.removeRow(getSelectedRowForDeletion);
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

