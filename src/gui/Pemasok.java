        try {
            pst = con.prepareStatement("INSERT into pemasok(id,nama,no_telepon,alamat)values(?,?,?,?)");
            int id   = Integer.parseInt(tfId.getText());
            pst.setString(1, tfId.getText());
            pst.setString(2, tfNama.getText());
            pst.setString(3, tfNoTelpon.getText());
            pst.setString(4, taAlamat.getText());

            pst.executeUpdate();

        } catch (SQLException ex) {
            Logger.getLogger(Pemasok.class.getName()).log(Level.SEVERE, null, ex);
        }

 
       // Menambahkan data ke dalam row
       int id   = Integer.parseInt(tfId.getText());
